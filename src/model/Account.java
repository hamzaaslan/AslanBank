package model;

public class Account {
    private String type;
    private String name;
    private int id;

    public Account(String type, String name) {
        this.type = this.type;
        this.name = this.name;
        this.id = id;
    }
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


}
